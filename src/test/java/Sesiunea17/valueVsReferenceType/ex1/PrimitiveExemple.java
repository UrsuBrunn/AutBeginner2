package Sesiunea17.valueVsReferenceType.ex1;

public class PrimitiveExemple {
    byte myByte=12;
    //-128 to 127
    short myShort=13;
    //16 bit of an integer values. -32767 to 32767

    //32 bit of integer value
    int myInt=14;

    //
    double myDouble=15.0;

    // 64 bit of integer values to a 15 decimal number
    float myFloat=16.0f;

    // 64 bit of integer value
    long myLong=17;

    // It is not only used with True and False and also used in condition evaluations
    boolean myBoolean=true;

    // Unicode value
    char myChar='A';




}
